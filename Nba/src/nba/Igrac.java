package nba;

public class Igrac extends Statistika{
	// nepotrebno je unositi statistiku kod igraca

	private String ime;
	private String prezime;
	private String nazivEkipe;
	
	public String getIme() {
		return ime;
	}
	public void setIme(String ime) {
		this.ime = ime;
	}
	public String getPrezime() {
		return prezime;
	}
	public void setPrezime(String prezime) {
		this.prezime = prezime;
	}
	public String getNazivEkipe() {
		return nazivEkipe;
	}
	public void setNazivEkipe(String nazivEkipe) {
		this.nazivEkipe = nazivEkipe;
	}

	
}

